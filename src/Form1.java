    private void tmblEq(){
        if(!stateOver){
            if(!stateEq){
                double temp;
                temp = numActual;
                numActual = tempActual;
                tempActual = temp;
            }
            double temps = numActual;
            switch(operator) {
                case '+':
                    numActual += tempActual;
                    lblDside.setText(df1.format(temps)+'+'+df1.format(tempActual)+'=');
                    lblDisplay.setText(df.format(numActual));
                    break;
                case '-':
                    numActual -= tempActual;
                    lblDside.setText(df1.format(temps)+'-'+df1.format(tempActual)+'=');
                    lblDisplay.setText(df.format(numActual));
                    break;
                case 'Ã—':
                    numActual *= tempActual;
                    lblDside.setText(df1.format(temps)+'Ã—'+df1.format(tempActual)+'=');
                    lblDisplay.setText(df.format(numActual));
                    break;
                case 'Ã·':
                    numActual /= tempActual;
                    lblDside.setText(df1.format(temps)+'Ã·'+df1.format(tempActual)+'=');
                    lblDisplay.setText(df.format(numActual));
                    break;
            }
            numDisplay="0";
            stateEq = true;
            stateComa = false;
            stateMin = false;
            sizing();
            addItem(lblDside.getText()+df.format(numActual));
        }
        else{
            resetState();
        }
        sizing();
        
    }
    private void tmblOpt(char opt){
        if(opt=='%'||opt=='Â²'){
            tempActual = numActual;
            lblDside.setText(df1.format(tempActual)+opt);
            operator=opt;
            double temp = tempActual;
            eqs();
            addItem(df1.format(temp)+opt+'='+df.format(tempActual));
            numDisplay = "0";
        }
        else{
            if(operator!=opt || stateEq){
                if(numDisplay!="0"){
                    eqs();
                }
                tempActual = numActual;
                lblDside.setText(df1.format(tempActual)+opt);
                operator=opt;
            }
            else if(numDisplay!="0"){
                eqs();
                addItem(df1.format(numActual-Double.valueOf(numDisplay))+opt+df1.format(Double.valueOf(numDisplay))+'='+df.format(numActual));
            }
            
            numDisplay = "0";
            stateComa = false;
            stateMin = false;
            stateEq = false;
        }
        sizing();
    }
